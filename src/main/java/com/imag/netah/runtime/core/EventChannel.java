package com.imag.netah.runtime.core;

/**
 *
 * @author epaln
 */
public class EventChannel {
    private String topic;

    public EventChannel(String topic) {
        this.topic = topic;
    }

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }

    @Override
    public String toString() {
        return topic;
    }
}
